let studyCounter = 0;
let careerCounter = 0;

function clampScore(value, fallback = 3) {
  const num = Number(value);
  if (!Number.isFinite(num)) return fallback;
  return Math.max(1, Math.min(5, Math.round(num)));
}

function toList(value) {
  return Array.isArray(value) ? value.filter(Boolean) : [];
}

function pickText(...values) {
  return values.map((value) => String(value || "").trim()).find(Boolean) || "";
}

function buildStudyDirection(group, item) {
  studyCounter += 1;
  const tags = Array.from(new Set([
    group.group,
    group.category,
    ...(group.tags || []),
    ...(item.tags || []),
  ])).filter(Boolean);
  const aliases = toList(item.aliases);
  const keywords = Array.from(new Set([
    ...(group.keywords || []),
    ...(item.keywords || []),
    ...tags,
  ])).filter(Boolean);
  const fitSignals = Array.from(new Set([
    ...(group.fitSignals || []),
    ...(item.fitSignals || []),
  ])).filter(Boolean);
  const antiSignals = Array.from(new Set([
    ...(group.antiSignals || []),
    ...(item.antiSignals || []),
  ])).filter(Boolean);
  const careerExits = Array.from(new Set([
    ...(group.careerExits || []),
    ...(item.careerExits || []),
  ])).filter(Boolean);
  const focus = pickText(item.focus, group.focus, `${item.title}相关课程、方法与研究应用`);
  const futureValueHint = pickText(
    item.futureValueHint,
    group.futureValueHint,
    `这个方向和 ${pickText(group.futureTheme, "数字化与行业升级")} 结合紧，后续出口相对清晰。`
  );

  return {
    id: `study_${String(studyCounter).padStart(3, "0")}`,
    type: "study",
    category: group.category,
    parentDiscipline: group.group,
    title: item.title,
    aliases,
    tags,
    keywords,
    summary: `${item.title} 主要研究 ${focus}。`,
    whatItIs: `${item.title} 主要研究 ${focus}。`,
    why: pickText(
      item.why,
      group.whyTemplate ? group.whyTemplate.replace(/\{title\}/g, item.title) : "",
      `适合想把 ${pickText(item.valueFocus, group.valueFocus, "学术兴趣、方法训练和职业出口")} 连起来的人。`
    ),
    risk: pickText(item.risk, group.risk, "如果课程基础、项目证据和申请动机讲不清，竞争力会被拉低。"),
    nextStep: pickText(item.nextStep, group.nextStep, `先补 1 条最能支撑 ${item.title} 的课程、项目或研究动机。`),
    futureValueHint,
    fitSignals,
    antiSignals,
    degreeLevel: pickText(item.degreeLevel, group.degreeLevel, "硕士常见"),
    degreeScore: clampScore(item.degreeScore ?? group.degreeScore, 3),
    salaryScore: clampScore(item.salaryScore ?? group.salaryScore, 3),
    frontierScore: clampScore(item.frontierScore ?? group.frontierScore, 3),
    scarcityScore: clampScore(item.scarcityScore ?? group.scarcityScore, 3),
    aiRelevanceScore: clampScore(item.aiRelevanceScore ?? group.aiRelevanceScore, 3),
    futurePotentialScore: clampScore(item.futurePotentialScore ?? group.futurePotentialScore, 3),
    careerExits,
    relatedDirections: Array.from(new Set([
      ...(group.relatedDirections || []),
      ...(item.relatedDirections || []),
    ])).filter(Boolean),
  };
}

